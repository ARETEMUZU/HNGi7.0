            <blockquote><strong>Mark Essien Steps Down as HNG Internship CEO</strong><br>-By Hayjay Omojuwa on April 26, 2017</blockquote>
        </figure>
    <div class="para"> 
        <p>The HNG Internship, a program that provides free internship to Africans who are interested in coding, has appointed Seyi Fade as the new Chief Executive Officer (CEO). The appointment follows the decision of Mark Essien to step down.</p>

        <p>The appointment was revealed by Mark Essien on his official Twitter page. He, however, said that he will still be involved in the HNG Internship process and retains his designation as ‘co-founder’.</p>

        <p>His replacement, Seyi Fade, is a python and JavaScript developer. As the new CEO, Fade will now run the operations of the Internship. He is expected to improve the internship’s culture of stability and support for trainees.</p>
        <p>The internship program makes use of weekly tasks along with an education system that combines technology with a system of learning to mentor interns. The intern coders are introduced to complex programming frameworks and get to work on real-world software.</p>
        <p>According to Essien, the vision of HNG Internship remains the same. He reaffirmed their goal to “massively accelerate Nigerians and Africans becoming software developers”.</p>
        <p>He assured that the change in CEO will not affect the operations of the program but experimentations will be reduced as the program will adopt best practices from earlier programs.</p>
